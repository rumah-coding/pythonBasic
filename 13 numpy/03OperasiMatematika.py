import numpy as np
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])
print(a + b)  # Penjumlahan
print(a - b)  # Pengurangan
print(a * b)  # Perkalian elemen per elemen
print(a / b)  # Pembagian elemen per elemen
print(a ** 2)  # Perpangkatan elemen per elemen
print(np.sqrt(a))  # Akar kuadrat elemen per elemen
print(np.sin(a))  # Sinus elemen per elemen
print(np.cos(a))  # Cosinus elemen per elemen
print(np.tan(a))  # Tangen elemen per elemen
print(np.log(a))  # Logaritma elemen per elemen