# Deklarasi dan Alokasi Nilai Variabel
x = 10  # x diinisialisasi dengan nilai 10
y = "Hello"  # y diinisialisasi dengan string 'Hello'

# Mengakses dan Menampilkan Nilai Variabel
print("Nilai variabel x:", x)
print("Nilai variabel y:", y)

# Perubahan Nilai Variabel
x = 20  # nilai variabel x diubah menjadi 20
y = "World"  # nilai variabel y diubah menjadi 'World'

# Mengakses dan Menampilkan Nilai Variabel yang telah berubah
print("Nilai variabel x setelah perubahan:", x)
print("Nilai variabel y setelah perubahan:", y)

# Perhitungan Sederhana dengan Variabel
z = x + 5  # z diinisialisasi dengan hasil penjumlahan nilai variabel x dan 5

# Mengakses dan Menampilkan Nilai Variabel yang hasil dari operasi matematika
print("Nilai variabel z:", z)